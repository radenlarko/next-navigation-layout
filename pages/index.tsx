import Head from "next/head";
import { Inter } from "next/font/google";
import { Box, HStack, Text } from "@chakra-ui/react";
import { useRef, useState } from "react";
import debounce from "lodash.debounce";

const inter = Inter({ subsets: ["latin"] });

const dataMenu = ["Home", "Profile", "About", "Contact"];

export default function Home() {
  const [menuProp, setMenuProp] = useState("");

  const setMenuPropDebounce = useRef(
    debounce((value: string) => setMenuProp(value), 300)
  ).current;
  return (
    <>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className={inter.className}>
        <Box bg="gray.100" minH="100vh">
          <Box bg="white" role="group" onMouseLeave={() => setMenuProp("")}>
            <HStack maxW="7xl" h={50} alignItems="center" mx="auto">
              {dataMenu.map((item) => (
                <Box
                  key={item}
                  cursor="pointer"
                  color={menuProp === item ? "pink.400" : "inherit"}
                  _hover={{ color: "pink.400" }}
                  onMouseOver={() => setMenuPropDebounce(item)}
                >
                  <Text>{item}</Text>
                </Box>
              ))}
            </HStack>
            <Box
              maxW="7xl"
              mx="auto"
              minH="300px"
              display="none"
              _groupHover={{ display: "block" }}
            >
              <Text>Tes {menuProp}</Text>
            </Box>
          </Box>
        </Box>
      </main>
    </>
  );
}
